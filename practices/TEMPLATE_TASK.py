def export_report():
    """Генерирует отчет о выполнении практики в формате Markdown"""
    if "TODO" in STUDENT_INFO["full_name"]:
        print("❌ ОШИБКА: Вы не заполнили информацию о студенте (Секция 1).")
        return

    report = f"# Отчет по практике: {STUDENT_INFO['full_name']}\n\n"
    report += f"**Группа:** {STUDENT_INFO['group']}\n"
    report += f"**Дата:** {STUDENT_INFO['date']}\n\n"
    
    report += "## 1. Журнал промптов\n\n"
    if not PROMPT_LOGS:
        report += "⚠️ *Журнал пуст. Заполните список PROMPT_LOGS.*\n"
    
    for log in PROMPT_LOGS:
        report += f"### Задача {log.task_id} ({log.model})\n"
        report += f"**Prompt:**\n```text\n{log.prompt}\n```\n"
        report += f"**Результат:** {log.result_summary}\n"
        if log.refinement:
            report += f"**Улучшение:** {log.refinement}\n"
        report += "---\n"

    report += "\n## 2. Рефлексия\n\n"
    report += f"**Инсайт:** {REFLECTION['key_takeaway'].strip()}\n\n"
    report += f"**Критика AI:** {REFLECTION['tool_critique'].strip()}\n\n"
    report += f"**Оценка времени:** {REFLECTION['time_saved_estimate']}\n"

    # Сохранение
    os.makedirs("artifacts", exist_ok=True)
    with open("artifacts/report.md", "w", encoding="utf-8") as f:
        f.write(report)
    
    print(f"✅ Отчет успешно сгенерирован: artifacts/report.md")
    print("Теперь проверьте созданный файл, добавьте недостающие артефакты и отправляйте на проверку.")

